package ejerciciosPractice;

public class operadoresPlus {
    public static void main(String[] args) {
        int numero = 10;


        numero += 5;

        System.out.println(numero);

        numero -= 2;
        System.out.println(numero);

        numero *= 5;
        System.out.println(numero);

        numero /= 5;
        System.out.println(numero);

        numero %= 5;
        System.out.println(numero);



    }
}
